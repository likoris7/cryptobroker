function changeLanguage(langCode) {
    if(!translations[langCode]) return;
    
    // Save to localStorage
    localStorage.setItem('nextrade_lang', langCode);
    
    // Update all elements with data-i18n attribute
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if(translations[langCode][key]) {
            el.innerHTML = translations[langCode][key]; // Using innerHTML to maintain styling inner tags like <i>
        }
    });
    
    // Update active state of language buttons
    const langBtns = document.querySelectorAll('.lang-btn');
    if(langBtns) {
        langBtns.forEach(btn => btn.classList.remove('active-lang'));
        const activeBtn = document.querySelector(`.lang-btn[data-lang="${langCode}"]`);
        if(activeBtn) activeBtn.classList.add('active-lang');
    }
}

// Ensure lang loaded immediately
document.addEventListener('DOMContentLoaded', () => {
    let savedLang = localStorage.getItem('nextrade_lang') || 'en';
    changeLanguage(savedLang);
    
    const langBtns = document.querySelectorAll('.lang-btn');
    langBtns.forEach(btn => {
        btn.addEventListener('click', (e) => {
            e.preventDefault();
            changeLanguage(btn.getAttribute('data-lang'));
        });
    });
});
